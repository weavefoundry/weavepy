def _unpack_u24(b64):
    raw = _b64.b64decode(b64)
    out = [0] * (len(raw) // 3)
    for i in range(len(out)):
        j = i * 3
        out[i] = (raw[j] << 16) | (raw[j + 1] << 8) | raw[j + 2]
    return out


